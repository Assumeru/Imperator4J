package com.ee.imperator.exception;

public class ParamPageException extends PageException {
	private static final long serialVersionUID = 8766224349477949176L;

	public ParamPageException() {
		super();
	}

	public ParamPageException(String message, Throwable cause) {
		super(message, cause);
	}

	public ParamPageException(String message) {
		super(message);
	}

	public ParamPageException(Throwable cause) {
		super(cause);
	}
}
